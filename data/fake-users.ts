export const getRandomUser = (): FakeUser => {
    return fakeUsers[Math.floor(Math.random() * fakeUsers.length)];
};

export const getRandomUsers = (count: number): FakeUser[] => {
    const shuffled = [...fakeUsers].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, Math.min(count, fakeUsers.length));
};

export const getUserByUsername = (username: string): FakeUser | undefined => {
    return fakeUsers.find(user => user.username === username);
};
